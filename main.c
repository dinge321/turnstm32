void num(int n) {
    if (n == 0) {
    	GPIOC->ODR |= 0x0000120;
    	GPIOB->ODR |= 0x0000100;
    	GPIOA->ODR |= 0x0001860;
    } else if (n == 1) {
    	GPIOB->ODR |= 0x0000100;
    	GPIOA->ODR |= 0x0000060;
    }
    else if(n==2){
    	GPIOC->ODR |= 0x0000120;
    	GPIOA->ODR |= 0x00010E0;
    }
    else if(n==3){
    	GPIOC->ODR |= 0x0000120;
    	GPIOA->ODR |= 0x00018C0;
    }
    else if(n==4){
    	GPIOC->ODR |= 0x0000020;
		GPIOB->ODR |= 0x0000100;
		GPIOA->ODR |= 0x00008C0;
    }
    else if(n==5){
		GPIOA->ODR |= 0x00018C0;
		GPIOB->ODR |= 0x0000100;
		GPIOC->ODR |= 0x0000100;
    }
    else if(n==6){
    	GPIOA->ODR |= 0x00018E0;
    	GPIOB->ODR |= 0x0000100;
    	GPIOC->ODR |= 0x0000100;
	}
    else if(n==7){
    	GPIOA->ODR |=0x0000880;
    	GPIOC->ODR |= 0x0000120;
    }
    else if(n==8){
    	GPIOA->ODR |= 0x00018E0;
    	GPIOB->ODR |= 0x0000100;
    	GPIOC->ODR |= 0x0000120;
	}
    else if(n==9){
    	GPIOA->ODR |= 0x00018C0;
    	GPIOB->ODR |= 0x0000100;
    	GPIOC->ODR |= 0x0000120;
	}
}
